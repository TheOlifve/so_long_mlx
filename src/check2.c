#include "../so_long.h"

int	map_line_len(char **map, int len)
{
	int	i;
	int	j;

	i = -1;
	j = 0;
	while (map && map[++i])
	{
		while (map[i][j])
			j++;
		if (j != len)
			return (0);
		j = 0;
	}
	return (1);
}
